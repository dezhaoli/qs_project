public class RegexSharding {

	private String databaseRule;
	private String tableRule;
	private String database;
	private String table;
	public String getDatabaseRule() {
		return databaseRule;
	}
	public void setDatabaseRule(String databaseRule) {
		this.databaseRule = databaseRule;
	}
	public String getTableRule() {
		return tableRule;
	}
	public void setTableRule(String tableRule) {
		this.tableRule = tableRule;
	}
	public String getDatabase() {
		return database;
	}
	public void setDatabase(String database) {
		this.database = database;
	}
	public String getTable() {
		return table;
	}
	public void setTable(String table) {
		this.table = table;
	}
	
	
}

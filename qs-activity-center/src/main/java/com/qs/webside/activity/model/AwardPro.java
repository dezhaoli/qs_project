package com.qs.webside.activity.model;

import java.io.Serializable;

public class AwardPro implements Serializable{

	private int id;
	private String name;
	private int num;
	private double gailv;
	private int type;

	
	public AwardPro(int id, String name, int num, double gailv,int type) {
		super();
		this.id = id;
		this.name = name;
		this.num = num;
		this.gailv = gailv;
		this.type = type;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public double getGailv() {
		return gailv;
	}

	public void setGailv(double gailv) {
		this.gailv = gailv;
	}

	@Override
	public String toString() {
		return "Award [id=" + id + ", name=" + name + ", num=" + num + ", gailv=" + gailv + "]";
	}

}
